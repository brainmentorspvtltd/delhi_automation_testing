public class DataTypes {
    public static void main(String[] args) {
        
        int a = 10;
        long b = 100l; // 100L
        int d = 130;
        byte c = (byte)d; // Explicit Conversion
        byte w = 10;
        long ee = w; // Implicit Conversion
        System.out.println(c);
        float tt = 90.20f; // f or F
        float rr2 = (float)90.20;
        double g1 = 10.205454533;
        char d1 = 'A';
        boolean flag = true;
    }
}