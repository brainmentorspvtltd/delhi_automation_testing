public class Loops {
    public static void main(String[] args) {
        // while
        // for 
        // do while
        int i = 1;
        while(i<=10){
            System.out.println("I is "+i);
            if(i==5){
                i+=2;
            }
            i++;
        }
        // Menus (Choices)
        do{
            System.out.println("I is "+i);
            i++;
        }while(i<=10);
        // 
        for(i =1; i<=10; i++){
            System.out.println("I is "+i);
        }
    }
    
}